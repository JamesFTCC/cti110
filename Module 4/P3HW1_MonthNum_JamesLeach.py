# Inputs number of month from user
# Determines the name of the month based on the value inputted
# Outputs the name of the month

month=float(input('What is the number of the month? '))

if month==1:
    print('January')
elif month==2:
    print('February')
elif month==3:
    print('March')
elif month==4:
    print('April')
elif month==5:
    print('May')
elif month==6:
    print('June')
elif month==7:
    print('July')
elif month==8:
    print('August')
elif month==9:
    print('September')
elif month==10:
    print('October')
elif month==11:
    print('November')
elif month==12:
    print('December')
else:
    print('Number not in range.')
